using UnityEngine;

public abstract class Health : MonoBehaviour
{
    [SerializeField] private float _health;

    internal void TakeDamage(float damage)
    {
        if (_health < 0)
            return;
        
        if(damage < 0)
            return;
        
        _health -= damage;

        if (_health <= 0)
        {
            _health = 0;
            Die();
        }
    }

    internal abstract void Die();
}